package com.byMyself;

public class IfElse {
	
	public static void main(String[] args) {
		
		//simple if statement
		
		//variables
		int AB = 10;
		double DE = 9.9;
		
		
		// if statement => if(condition){}
		if (AB <= DE) {
			System.out.println("I am greater!");
		} else if (AB == DE) {
			System.out.println("You are rounding up");
		}
		else {
			System.out.println("I am confusion");
		}
	}

}
